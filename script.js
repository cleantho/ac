var matriz = [AC, AL, AM, AP, BA, CE, DF, ES, GO, MA, MG, MS, MT, PA, PB, PE, PI, PR, RJ, RN, RO, RR, RS, SC, SE, SP, TO];

function funcao(){
	var x = Number(document.getElementById("origem").value);
	var y = Number(document.getElementById("destino").value);
	var cio = document.getElementsByName("ori");
	var cid = document.getElementsByName("dest");
	var o;
	var d;
	for (var i = 0; i < 2; i++){
		if(cio[i].checked){
			o = cio[i].value;
		}
		if(cid[i].checked){
			d = cid[i].value;
		}
	}
	var letra = ' ';
	if(x == y){
		if(o == 'c' && d == 'c'){
			letra = 'L';
		}
		else{
			letra = 'E';
		}			
	}else{
		if(o == 'c' && d == 'c'){
			letra = 'N';
		}
		else{
			letra = 'I';
		}			
	}		
	var res = document.getElementById("resultado");        
	res.innerHTML = letra + matriz[x][y];
	res.style.opacity = 1;
}

function mudou(){
	var res = document.getElementById("resultado");
	res.style.opacity = 0;
}